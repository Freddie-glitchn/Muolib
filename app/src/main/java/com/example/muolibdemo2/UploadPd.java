package com.example.muolibdemo2;

public class UploadPd {

    public String name;
    public String url;

    public UploadPd(String name, String url) {
        this.name = name;
        this.url = url;
    }

    public UploadPd() {
    }

    public String getName() {
        return name;
    }


    public String getUrl() {
        return url;
    }

}
